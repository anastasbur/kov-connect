// Помощники для корректного русского языка: склонение числительных
// и предложный падеж названий стран ("в Германии", "на Кипре", "во Франции").

/**
 * Возвращает правильную форму существительного для числа.
 * forms = [для 1, для 2–4, для 0/5–20], например:
 * plural(3, ["материал", "материала", "материалов"]) → "материала"
 */
export function plural(n: number, forms: [string, string, string]): string {
  const abs = Math.abs(n) % 100;
  const last = abs % 10;
  if (abs > 10 && abs < 20) return forms[2];
  if (last > 1 && last < 5) return forms[1];
  if (last === 1) return forms[0];
  return forms[2];
}

/** Число + правильно склонённое существительное: pluralize(3, MATERIALS) → "3 материала" */
export function pluralize(n: number, forms: [string, string, string]): string {
  return `${n} ${plural(n, forms)}`;
}

