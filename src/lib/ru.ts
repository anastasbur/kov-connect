/**
 * Возвращает фразу с предлогом в предложном падеже: "в Германии", "на Кипре".
 * Для незнакомых названий применяет общее правило склонения.
 */
export function locative(nameRu: string): string {
  const name = (nameRu ?? "").trim();
  if (LOCATIVE[name]) return LOCATIVE[name];
  if (/ия$/.test(name)) return `в ${name.slice(0, -2)}ии`;
  if (/[ая]$/.test(name)) return `в ${name.slice(0, -1)}е`;
  return `в ${name}е`;
}
